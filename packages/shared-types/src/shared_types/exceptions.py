# 定义跨 API、存储和测试环境复用的领域异常，避免底层模块依赖 FastAPI。


class NotFoundError(Exception):
    """存储层实体未找到。与 FastAPI HTTPException 解耦，可在非 HTTP 上下文复用。"""


class ConflictError(Exception):
    """实体冲突（如重复创建）。"""
